# Task 1: Format the float pi to display with 3 decimal places
pi_value = 3.141592653589793
formatted_pi = f"{pi_value:.3f}"
print("Formatted Pi:", formatted_pi)

# Task 2: Format the float price to display with 2 decimal places and a dollar sign
price = 29.99
formatted_price = f"${price:.2f}"
print("Formatted Price:", formatted_price)